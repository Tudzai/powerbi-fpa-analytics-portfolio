from __future__ import annotations

import csv
import json
from collections import Counter
from datetime import date
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parents[2]
RAW_DIR = PROJECT_ROOT / "data" / "raw"
DATA_DIR = PROJECT_ROOT / "data"
MODEL_DIR = PROJECT_ROOT / "model"


def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def profile_file(path: Path) -> dict[str, object]:
    rows = read_csv(path)
    columns = list(rows[0].keys()) if rows else []
    date_values = [r["MonthStart"] for r in rows if "MonthStart" in r and r["MonthStart"]]
    missing = {
        column: sum(1 for row in rows if row.get(column, "") == "")
        for column in columns
    }
    profile: dict[str, object] = {
        "file": str(path.relative_to(PROJECT_ROOT)).replace("\\", "/"),
        "rows": len(rows),
        "columns": len(columns),
        "column_names": columns,
        "missing_values": {k: v for k, v in missing.items() if v},
    }
    if date_values:
        profile["main_date_column"] = "MonthStart"
        profile["date_min"] = min(date_values)
        profile["date_max"] = max(date_values)
    return profile


def sum_measure(rows: list[dict[str, str]], measure: str, scenario: str | None = None, month: str | None = None) -> float:
    total = 0.0
    for row in rows:
        if scenario and row.get("Scenario") != scenario:
            continue
        if month and row.get("MonthStart") != month:
            continue
        total += float(row.get(measure, 0) or 0)
    return round(total, 2)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def main() -> None:
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    MODEL_DIR.mkdir(parents=True, exist_ok=True)

    raw_files = sorted(RAW_DIR.glob("*.csv"))
    if not raw_files:
        raise FileNotFoundError(f"No raw CSV files found in {RAW_DIR}. Run 00_generate_sample_raw.py first.")

    profiles = [profile_file(path) for path in raw_files]
    financials = read_csv(RAW_DIR / "raw_fpa_financials.csv")
    bridge = read_csv(RAW_DIR / "raw_fpa_budget_actual_bridge.csv")

    latest_actual_month = max(r["MonthStart"] for r in financials if r["Scenario"] == "Actual")
    scenario_counts = Counter(r["Scenario"] for r in financials)
    bu_counts = Counter(r["BusinessUnit"] for r in financials)

    summary = {
        "project": "Monthly FP&A Performance Pack",
        "source_type": "Synthetic portfolio sample data generated by build/scripts/00_generate_sample_raw.py",
        "latest_complete_actual_month": latest_actual_month,
        "source_files": profiles,
        "tables": [
            {
                "name": "raw_fpa_financials",
                "grain": "one row per month, scenario, business unit, product, region, customer",
                "rows": len(financials),
                "scenario_distribution": dict(scenario_counts),
                "business_unit_distribution": dict(bu_counts),
            },
            {
                "name": "raw_fpa_opex_department",
                "grain": "one row per month, scenario, business unit, region, department",
            },
            {
                "name": "raw_fpa_cash",
                "grain": "one row per month, scenario, business unit, region",
            },
            {
                "name": "raw_fpa_budget_actual_bridge",
                "grain": "one row per latest month, business unit, region, bridge step",
                "rows": len(bridge),
            },
        ],
        "kpi_totals": {
            "latest_actual_month": latest_actual_month,
            "actual_revenue_latest_month": sum_measure(financials, "Revenue", "Actual", latest_actual_month),
            "budget_revenue_latest_month": sum_measure(financials, "Revenue", "Budget", latest_actual_month),
            "forecast_revenue_latest_month": sum_measure(financials, "Revenue", "Forecast", latest_actual_month),
            "actual_gross_margin_latest_month": sum_measure(financials, "GrossMargin", "Actual", latest_actual_month),
            "actual_ebitda_latest_month": sum_measure(financials, "EBITDA", "Actual", latest_actual_month),
            "actual_orders_latest_month": sum_measure(financials, "Orders", "Actual", latest_actual_month),
        },
        "data_quality_flags": [
            "Synthetic portfolio dataset, not company actuals.",
            "CashBalance is a month-end balance; Power BI measures must use latest visible month instead of summing across time.",
            "Department Opex is modeled in a separate fact table; customer/product EBITDA uses allocated opex in fact_financials.",
        ],
    }
    (DATA_DIR / "source_summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")

    quality_lines = [
        "# Data Quality Report",
        "",
        "## Source Status",
        "",
        "- Source type: synthetic FP&A portfolio sample.",
        f"- Latest complete actual month: {latest_actual_month}.",
        "- Generated raw files are deterministic from `build/scripts/00_generate_sample_raw.py`.",
        "- No source rows are manually edited in `data/raw/`.",
        "",
        "## File Profile",
        "",
    ]
    for profile in profiles:
        quality_lines.extend(
            [
                f"### {profile['file']}",
                "",
                f"- Rows: {profile['rows']}",
                f"- Columns: {profile['columns']}",
            ]
        )
        if "date_min" in profile:
            quality_lines.append(f"- Date range: {profile['date_min']} to {profile['date_max']}")
        if profile["missing_values"]:
            quality_lines.append(f"- Missing values: {profile['missing_values']}")
        else:
            quality_lines.append("- Missing values: none detected.")
        quality_lines.append("")

    quality_lines.extend(
        [
            "## Key Controls",
            "",
            "- Revenue, COGS, gross margin, opex, EBITDA, orders, and cash impact are positive numeric fields.",
            "- Gross margin equals Revenue minus COGS in source, within rounding tolerance.",
            "- EBITDA equals GrossMargin minus AllocatedOpex in source, within rounding tolerance.",
            "- Budget and Forecast cover Jan 2026-Dec 2026; Actual covers Jan 2025-May 2026.",
            "",
            "## Known Caveats",
            "",
            "- This package is build-ready for Power BI but is not final PBIX until opened, refreshed, and QA-tested in Power BI Desktop.",
            "- Forecast values are sample management assumptions, not a statistically generated forecast model.",
        ]
    )
    write_text(DATA_DIR / "data_quality_report.md", "\n".join(quality_lines) + "\n")

    dictionary = """# Data Dictionary

## Raw Tables

### raw_fpa_financials

Grain: one row per month, scenario, business unit, product, region, customer.

| Column | Meaning |
|---|---|
| RecordID | Stable source row id. |
| MonthStart | First day of the financial month. |
| Scenario | Actual, Budget, or Forecast. |
| BusinessUnit | FP&A reporting business unit. |
| Product | Service/product line within business unit. |
| Region | Reporting region. |
| Customer | Customer or grouped long-tail customer bucket. |
| CustomerSegment | Strategic, Enterprise, Growth, or SMB. |
| Industry | Customer industry. |
| Revenue | Net revenue for the row. |
| COGS | Cost of goods/services for the row. |
| GrossMargin | Revenue less COGS. |
| AllocatedOpex | Allocated operating expense for segment drill-down. |
| EBITDA | GrossMargin less AllocatedOpex. |
| Orders | Operational volume proxy. |
| CashImpact | Estimated cash contribution for the row. |

### raw_fpa_opex_department

Grain: one row per month, scenario, business unit, region, department.

### raw_fpa_cash

Grain: one row per month, scenario, business unit, region. CashBalance is a month-end balance.

### raw_fpa_budget_actual_bridge

Grain: one row per May 2026 bridge step, business unit, region.

### raw_monthly_commentary

Grain: one row per monthly FP&A narrative.
"""
    write_text(MODEL_DIR / "data_dictionary.md", dictionary)

    print(f"Wrote source summary, quality report, and data dictionary for {len(raw_files)} raw files.")


if __name__ == "__main__":
    main()
